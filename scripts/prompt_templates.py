def output_rules(desc: str) -> str:
    lo, hi = LENGTH_BY_DESCRIPTION.get(desc, (400, 700))
    mid = (lo + hi) // 2
    return f"""【輸出要求】
- 只輸出內文段落，不要標題、不要 JSON、不要 markdown
- 全文字數約 {lo}–{hi} 字（以約 {mid} 字為佳），2–4 段，段與段之間空一行
- 不要用「總之」「綜上所述」收尾"""


def base(desc: str, task: str, extra: str = "") -> str:
    parts = [
        PERSONA,
        "",
        USER_BLOCK,
        "",
        CONTEXT_BLOCK,
        "",
        f"【本篇要寫什麼】{task}",
        output_rules(desc),
    ]
    if extra:
        parts.append(extra)
    return "\n".join(parts)


